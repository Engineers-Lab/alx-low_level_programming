#include <stdio.h>
/**
*    main - print types sizes
*
*    Description: This function prints the various sizes
*    of types on the computer that is run on
*    Return: 0 if the program succed
*/
int main(void)
{
	printf("Size of a char: %c byte(s)\n", sizeof(char));
	printf("Size of an int: %d byte(s)\n", sizeof(int));
	printf("Size of a long int: %ld byte(s)\n", sizeof(long int));
	printf("Size of long long int: %lld byte(s)\n", sizeof(long long int));
	printf("Size of float: %f byte(s)\n", sizeof(float));
	return (0);
}
